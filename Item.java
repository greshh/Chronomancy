public class Item {
    public String name, description;
    public double x, y;
    public double width, height;

    public Item(String name, String description, double x, double y, double width, double height) {
        this.name = name;
        this.description = description;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
